const promoteCommand = {
    name: "promote",
    category: "group",
    desc: "Give a member Admin privileges",
    async execute(sock, msg, args, { from, isMe, prefix }) {
        // --- рҹӣЎпёҸ GROUP-ONLY SHIELD ---
        if (!from.endsWith('@g.us')) return;

        // --- рҹ“Ҡ PERMISSION CHECKS ---
        const metadata = await sock.groupMetadata(from);
        const participants = metadata.participants;
        const admins = participants.filter(p => p.admin).map(p => p.id);
        
        const sender = msg.key.participant || from;
        const botId = sock.user.id.split(':')[0] + '@s.whatsapp.net';
        
        const isBotAdmin = admins.includes(botId);
        const isAdmin = admins.includes(sender) || isMe;

        if (!isAdmin) {
            return sock.sendMessage(from, { 
                text: `в”Ңв”ҖгҖҺ бҙ _КңбҙңКҷ sбҙҮбҙ„бҙңКҖЙӘбҙӣКҸ гҖҸ\nв”Ӯ вҡҷ *бҙҖКҹбҙҮКҖбҙӣ:* бҙҖбҙ…бҙҚЙӘЙҙ бҙҳКҖЙӘбҙ ЙӘКҹбҙҮЙўбҙҮ КҖбҙҮЗ«бҙңЙӘКҖбҙҮбҙ….\nв””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ҳ` 
            });
        }

        if (!isBotAdmin) {
            return sock.sendMessage(from, { 
                text: `в”Ңв”ҖгҖҺ sКҸsбҙӣбҙҮбҙҚ_бҙҮКҖКҖ гҖҸ\nв”Ӯ вҡҷ *бҙҮКҖКҖбҙҸКҖ:* КҷбҙҸбҙӣ ЙҙбҙҮбҙҮбҙ…s бҙҖбҙ…бҙҚЙӘЙҙ sбҙӣбҙҖбҙӣбҙңs.\nв””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ҳ` 
            });
        }

        // --- рҹҺҜ TARGET DETECTION ---
        let users = msg.message?.extendedTextMessage?.contextInfo?.mentionedJid || [];
        if (msg.message?.extendedTextMessage?.contextInfo?.participant) {
            users.push(msg.message.extendedTextMessage.contextInfo.participant);
        }

        // Remove duplicates and Bot safety
        users = [...new Set(users)].filter(u => u !== botId);

        if (!users.length) {
            return sock.sendMessage(from, { 
                text: `в”Ңв”ҖгҖҺ бҙңsбҙҖЙўбҙҮ_ЙӘЙҙТ“бҙҸ гҖҸ\nв”Ӯ вҡҷ *бҙ„бҙҸбҙҚбҙҚбҙҖЙҙбҙ…:* ${prefix}бҙҳКҖбҙҸбҙҚбҙҸбҙӣбҙҮ [КҖбҙҮбҙҳКҹКҸ/бҙӣбҙҖЙў]\nв”Ӯ вҡҷ *бҙҖЙӘбҙҚ:* бҙҮКҹбҙҮбҙ бҙҖбҙӣбҙҮ бҙҖбҙңбҙӣКңбҙҸКҖЙӘбҙӣКҸ\nв””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ҳ` 
            });
        }

        try {
            // --- рҹҡҖ EXECUTION ---
            await sock.sendMessage(from, { react: { text: "рҹ‘‘", key: msg.key } });
            await sock.groupParticipantsUpdate(from, users, "promote");

            // --- рҹ“‘ MODERATION LOG ---
            let promoteLog = `в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ҳ\n`;
            promoteLog += `в”Ӯ      *бҙҖбҙңбҙӣКңбҙҸКҖЙӘбҙӣКҸ_бҙҮКҹбҙҮбҙ бҙҖбҙӣбҙҮбҙ…* \n`;
            promoteLog += `в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ҳ\n\n`;
            promoteLog += `в”Ңв”ҖгҖҺ sбҙӣбҙҖбҙӣбҙңs_КҹбҙҸЙў гҖҸ\n`;
            promoteLog += `в”Ӯ рҹ‘‘ *бҙҖбҙ„бҙӣЙӘбҙҸЙҙ:* бҙҳКҖбҙҸбҙҚбҙҸбҙӣбҙҮ_sбҙңбҙ„бҙ„бҙҮss\n`;
            promoteLog += `в”Ӯ рҹ‘Ө *бҙӣбҙҖКҖЙўбҙҮбҙӣ(s):* ${users.length}\n`;
            promoteLog += `в”Ӯ рҹ‘® *бҙҖбҙ…бҙҚЙӘЙҙ:* @${sender.split('@')[0]}\n`;
            promoteLog += `в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ҳ\n\n`;
            promoteLog += `_ЙӘЙҙкң°ЙӘЙҙЙӘбҙӣбҙҮ ЙӘбҙҚбҙҳбҙҖбҙ„бҙӣ x бҙ ЙӘЙҙЙҙЙӘбҙҮ бҙ…ЙӘЙўЙӘбҙӣбҙҖКҹ_`;

            await sock.sendMessage(from, { 
                text: promoteLog, 
                mentions: [sender, ...users] 
            });

        } catch (err) {
            await sock.sendMessage(from, { 
                text: `в”Ңв”ҖгҖҺ sКҸsбҙӣбҙҮбҙҚ_бҙҮКҖКҖ гҖҸ\nв”Ӯ вҡҷ *КҹбҙҸЙў:* ${err.message}\nв””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ҳ` 
            });
        }
    }
};

export default promoteCommand;
